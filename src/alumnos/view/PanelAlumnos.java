    public void recargarDatos() {
        if (alumnoController == null) return;

        String seleccionActual = (String) cboEstado.getSelectedItem();

        List<Alumno> datosActualizados = alumnoController.obtenerAlumnosPorEstado(seleccionActual);

        refrescarTabla(datosActualizados);
    }

    public void refrescarTabla(List<Alumno> alumnos) {
        this.alumnosCargados = (alumnos != null) ? alumnos : Collections.emptyList();
        DefaultTableModel modelo = (DefaultTableModel) tablaAlumnos.getModel();
        modelo.setRowCount(0);

        for (Alumno a : this.alumnosCargados) {
            modelo.addRow(new Object[]{
                a.getCodigoEstudiante(),
                a.getDni(),
                a.getNombreCompleto(),
                (a.getApoderado() != null) ? a.getApoderado().getNombreCompleto() : "Sin Apoderado",
                (a.getApoderado() != null) ? a.getApoderado().getParentesco() : "N/A",
                (a.getApoderado() != null) ? a.getApoderado().getTelefono() : "N/A",
                a.isActivo() ? "ACTIVO" : "RETIRADO"
            });
        }
    }


   
